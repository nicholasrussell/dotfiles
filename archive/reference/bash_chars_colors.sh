for i in {0..256}; do
    eval "export CLR_${i}='\033[38;5;${i}m'"
done

function list_numbered_colors() {
    local clr
    local none="\$CLR_NONE"
    for i in {0..256}; do
        clr="\$CLR_${i}"
        printf "%s%3d%s " "$(eval echo -e $clr)" "$i" "$(eval echo -e $none)"
        if (( $(( (i + 1) % 11)) == 0 )); then
            printf "\n"
        fi
    done
    printf "\n"
}

function list_defined_colors() {
    local defined_colors=($(env | grep CLR_ | sort))
    local defined_color
    local clr_arr
    local clr_name
    local clr_code
    for defined_color in "${defined_colors[@]}"; do
        clr_arr=(${defined_color//=/ })
        clr_name=${clr_arr[0]}
        clr_code=${clr_arr[1]}
        echo -e "${clr_code}${clr_name}${CLR_NONE}"
    done
}

