function list_defined_characters() {
    local defined_chars=($(env | grep CHR_ | sort))
    local defined_char
    local char_arr
    local char_name
    local char_code
    for defined_char in "${defined_chars[@]}"; do
        char_arr=(${defined_char//=/ })
        char_name=${char_arr[0]}
        char_code=${char_arr[1]}
        printf "%40s %7s %s\n" $char_name $char_code $(echo -e $char_code)
    done
}

